import pandas as pd
import numpy as np

from sklearn.model_selection import train_test_split
from sklearn.metrics import accuracy_score

from sklearn.linear_model import LogisticRegression
from sklearn.tree import DecisionTreeClassifier
from sklearn.ensemble import RandomForestClassifier
from sklearn.svm import SVC
from sklearn.naive_bayes import GaussianNB

from imblearn.over_sampling import RandomOverSampler, SMOTE
from imblearn.under_sampling import RandomUnderSampler
from imblearn.combine import SMOTETomek, SMOTEENN

# Load dataset
df = pd.read_csv("Creditcard_data.csv")

# Display basic info
df.head()

df['Class'].value_counts()

X = df.drop(columns=['Class'])
y = df['Class']

samplers = {
    "Sampling1": RandomOverSampler(random_state=42),
    "Sampling2": RandomUnderSampler(random_state=42),
    "Sampling3": SMOTE(random_state=42),
    "Sampling4": SMOTETomek(random_state=42),
    "Sampling5": SMOTEENN(random_state=42)
}

models = {
    "M1": LogisticRegression(max_iter=1000),
    "M2": DecisionTreeClassifier(),
    "M3": RandomForestClassifier(),
    "M4": SVC(),
    "M5": GaussianNB()
}

results = pd.DataFrame(index=models.keys(), columns=samplers.keys())

for samp_name, sampler in samplers.items():
    X_res, y_res = sampler.fit_resample(X, y)

    X_train, X_test, y_train, y_test = train_test_split(
        X_res, y_res, test_size=0.3, random_state=42
    )

    for model_name, model in models.items():
        model.fit(X_train, y_train)
        y_pred = model.predict(X_test)
        acc = accuracy_score(y_test, y_pred) * 100
        results.loc[model_name, samp_name] = round(acc, 2)

results

results.idxmax(axis=1)

results.idxmax(axis=0)

results = results.astype(float)
